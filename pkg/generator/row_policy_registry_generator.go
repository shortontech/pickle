package generator

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"strconv"
	"strings"

	"github.com/shortontech/pickle/pkg/schema"
)

// GenerateRowPolicyRegistry emits desired PostgreSQL RLS state into the
// generated policy package. The policy runner reconciles it only during an
// explicit policy migration command.
type RowPolicyVersionState struct {
	PolicyID string
	Plans    []PostgresRowPolicyPlan
}

func GenerateRowPolicyRegistry(packageName string, plans []PostgresRowPolicyPlan, managed ...[]string) ([]byte, error) {
	return GenerateRowPolicyRegistryWithStates(packageName, plans, nil, managed...)
}

func GenerateRowPolicyRegistryWithStates(packageName string, plans []PostgresRowPolicyPlan, states []RowPolicyVersionState, managed ...[]string) ([]byte, error) {
	var b bytes.Buffer
	managedTables := rowPolicyManagedTables(plans, managed...)
	active := map[string]bool{}
	for _, plan := range plans {
		active[plan.Table] = true
	}
	b.WriteString("// Code generated by Pickle. DO NOT EDIT.\npackage " + packageName + "\n\n")
	b.WriteString("type GeneratedRowPolicyObject struct { Table, Name, Command, Using, WithCheck, Fingerprint string; Permissive bool }\n\n")
	b.WriteString("type GeneratedRowPolicyVersionState struct { DDL []string; Desired map[string][]string; Fingerprint string }\n\n")
	fingerprint := GeneratedRowPolicyFingerprint(plans)
	fmt.Fprintf(&b, "const GeneratedRowPolicyFingerprintValue = %s\n\n", strconv.Quote(fingerprint))
	b.WriteString("var GeneratedRowPolicyDDL = []string{\n")
	for _, helper := range PostgresPolicyIdentityHelpers() {
		fmt.Fprintf(&b, "\t%s,\n", strconv.Quote(helper))
	}
	for _, plan := range plans {
		table := quotePolicyIdent(plan.Table)
		if plan.Enable {
			fmt.Fprintf(&b, "\t%s,\n", strconv.Quote("ALTER TABLE "+table+" ENABLE ROW LEVEL SECURITY"))
		}
		if plan.Force {
			fmt.Fprintf(&b, "\t%s,\n", strconv.Quote("ALTER TABLE "+table+" FORCE ROW LEVEL SECURITY"))
		}
		for _, policy := range plan.Policies {
			fmt.Fprintf(&b, "\t%s,\n", strconv.Quote("DROP POLICY IF EXISTS "+quotePolicyIdent(policy.Name)+" ON "+table))
			q := "CREATE POLICY " + quotePolicyIdent(policy.Name) + " ON " + table + " FOR " + string(policy.Command) + " TO PUBLIC"
			if policy.Using != "" {
				q += " USING (" + policy.Using + ")"
			}
			if policy.WithCheck != "" {
				q += " WITH CHECK (" + policy.WithCheck + ")"
			}
			fmt.Fprintf(&b, "\t%s,\n", strconv.Quote(q))
			comment := "COMMENT ON POLICY " + quotePolicyIdent(policy.Name) + " ON " + table + " IS " + quotePolicyLiteral("pickle:fingerprint:"+fingerprint)
			fmt.Fprintf(&b, "\t%s,\n", strconv.Quote(comment))
		}
	}
	for _, tableName := range managedTables {
		if active[tableName] {
			continue
		}
		table := quotePolicyIdent(tableName)
		fmt.Fprintf(&b, "\t%s,\n", strconv.Quote("ALTER TABLE "+table+" NO FORCE ROW LEVEL SECURITY"))
		fmt.Fprintf(&b, "\t%s,\n", strconv.Quote("ALTER TABLE "+table+" DISABLE ROW LEVEL SECURITY"))
	}
	b.WriteString("}\n\nvar GeneratedRowPolicyDesired = map[string][]string{\n")
	planByTable := map[string]PostgresRowPolicyPlan{}
	for _, plan := range plans {
		planByTable[plan.Table] = plan
	}
	for _, tableName := range managedTables {
		plan := planByTable[tableName]
		names := make([]string, len(plan.Policies))
		for i, p := range plan.Policies {
			names[i] = p.Name
		}
		sort.Strings(names)
		fmt.Fprintf(&b, "\t%s: {", strconv.Quote(tableName))
		for i, name := range names {
			if i > 0 {
				b.WriteString(", ")
			}
			b.WriteString(strconv.Quote(name))
		}
		b.WriteString("},\n")
	}
	b.WriteString("}\n")
	b.WriteString("\nvar GeneratedRowPolicyStates = map[string]GeneratedRowPolicyVersionState{\n")
	baselineFingerprint := GeneratedRowPolicyFingerprint(nil)
	fmt.Fprintf(&b, "\t\"\": {DDL: []string{")
	for _, statement := range rowPolicyStateDDL(nil, managedTables, baselineFingerprint) {
		fmt.Fprintf(&b, "%s,", strconv.Quote(statement))
	}
	b.WriteString("}, Desired: map[string][]string{")
	for _, table := range managedTables {
		fmt.Fprintf(&b, "%s:{},", strconv.Quote(table))
	}
	fmt.Fprintf(&b, "}, Fingerprint:%s},\n", strconv.Quote(baselineFingerprint))
	for _, state := range states {
		stateFingerprint := GeneratedRowPolicyFingerprint(state.Plans)
		fmt.Fprintf(&b, "\t%s: {DDL: []string{", strconv.Quote(state.PolicyID))
		for _, statement := range rowPolicyStateDDL(state.Plans, managedTables, stateFingerprint) {
			fmt.Fprintf(&b, "%s,", strconv.Quote(statement))
		}
		b.WriteString("}, Desired: map[string][]string{")
		for _, table := range managedTables {
			fmt.Fprintf(&b, "%s:{", strconv.Quote(table))
			for _, name := range rowPolicyDesiredNames(state.Plans, table) {
				fmt.Fprintf(&b, "%s,", strconv.Quote(name))
			}
			b.WriteString("},")
		}
		fmt.Fprintf(&b, "}, Fingerprint:%s},\n", strconv.Quote(stateFingerprint))
	}
	b.WriteString("}\n")
	b.WriteString("\nvar GeneratedRowPolicyCatalog = []GeneratedRowPolicyObject{\n")
	for _, plan := range plans {
		for _, policy := range plan.Policies {
			fmt.Fprintf(&b, "\t{Table:%s, Name:%s, Command:%s, Using:%s, WithCheck:%s, Fingerprint:%s, Permissive:true},\n", strconv.Quote(plan.Table), strconv.Quote(policy.Name), strconv.Quote(string(policy.Command)), strconv.Quote(policy.Using), strconv.Quote(policy.WithCheck), strconv.Quote(fingerprint))
		}
	}
	b.WriteString("}\n")
	src, err := format.Source(b.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting row policy registry: %w\n%s", err, b.String())
	}
	return src, nil
}

func rowPolicyDesiredNames(plans []PostgresRowPolicyPlan, table string) []string {
	var names []string
	for _, plan := range plans {
		if plan.Table == table {
			for _, policy := range plan.Policies {
				names = append(names, policy.Name)
			}
		}
	}
	sort.Strings(names)
	return names
}

func rowPolicyStateDDL(plans []PostgresRowPolicyPlan, managedTables []string, fingerprint string) []string {
	statements := append([]string{}, PostgresPolicyIdentityHelpers()...)
	active := map[string]bool{}
	for _, plan := range plans {
		active[plan.Table] = true
		table := quotePolicyIdent(plan.Table)
		if plan.Enable {
			statements = append(statements, "ALTER TABLE "+table+" ENABLE ROW LEVEL SECURITY")
		}
		if plan.Force {
			statements = append(statements, "ALTER TABLE "+table+" FORCE ROW LEVEL SECURITY")
		}
		for _, policy := range plan.Policies {
			statements = append(statements, "DROP POLICY IF EXISTS "+quotePolicyIdent(policy.Name)+" ON "+table)
			q := "CREATE POLICY " + quotePolicyIdent(policy.Name) + " ON " + table + " FOR " + string(policy.Command) + " TO PUBLIC"
			if policy.Using != "" {
				q += " USING (" + policy.Using + ")"
			}
			if policy.WithCheck != "" {
				q += " WITH CHECK (" + policy.WithCheck + ")"
			}
			statements = append(statements, q, "COMMENT ON POLICY "+quotePolicyIdent(policy.Name)+" ON "+table+" IS "+quotePolicyLiteral("pickle:fingerprint:"+fingerprint))
		}
	}
	for _, tableName := range managedTables {
		if active[tableName] {
			continue
		}
		table := quotePolicyIdent(tableName)
		statements = append(statements, "ALTER TABLE "+table+" NO FORCE ROW LEVEL SECURITY", "ALTER TABLE "+table+" DISABLE ROW LEVEL SECURITY")
	}
	return statements
}

func rowPolicyManagedTables(plans []PostgresRowPolicyPlan, managed ...[]string) []string {
	set := map[string]bool{}
	for _, plan := range plans {
		set[plan.Table] = true
	}
	for _, tables := range managed {
		for _, table := range tables {
			set[table] = true
		}
	}
	result := make([]string, 0, len(set))
	for table := range set {
		result = append(result, table)
	}
	sort.Strings(result)
	return result
}

func GenerateRowPolicyRuntimeRegistry(packageName string, policies []ResolvedRowPolicy) ([]byte, error) {
	var b bytes.Buffer
	b.WriteString("// Code generated by Pickle. DO NOT EDIT.\npackage " + packageName + "\n\nfunc init() {\n")
	for _, resolved := range policies {
		fmt.Fprintf(&b, "\tregisterRowPolicyRuntime(rowPolicyRuntimeDefinition{Table: %s, SubjectCombination: %s, EnforcementClass: %s, IdentityTypes: map[string]string{", strconv.Quote(resolved.Protection.Table), strconv.Quote(string(resolved.Protection.SubjectCombination)), strconv.Quote(resolved.EnforcementClass))
		identityNames := make([]string, 0, len(resolved.Identities))
		for name := range resolved.Identities {
			identityNames = append(identityNames, name)
		}
		sort.Strings(identityNames)
		for _, name := range identityNames {
			fmt.Fprintf(&b, "%s:%s,", strconv.Quote(name), strconv.Quote(string(resolved.Identities[name])))
		}
		b.WriteString("}, Rules: []rowPolicyRuntimeRule{\n")
		for _, rule := range resolved.Protection.Rules {
			fmt.Fprintf(&b, "\t\t{Key:%s, SubjectKind:%s, SubjectName:%s", strconv.Quote(rule.Key), strconv.Quote(string(rule.Subject.Kind)), strconv.Quote(rule.Subject.Name))
			writeRuntimePredicateField(&b, "Select", rule.Select)
			writeRuntimePredicateField(&b, "Insert", rule.Insert)
			writeRuntimePredicateField(&b, "UpdateOld", rule.UpdateOld)
			writeRuntimePredicateField(&b, "UpdateNew", rule.UpdateNew)
			writeRuntimePredicateField(&b, "Delete", rule.Delete)
			b.WriteString("},\n")
		}
		b.WriteString("\t}})\n")
	}
	b.WriteString("}\n")
	src, err := format.Source(b.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting row policy runtime registry: %w\n%s", err, b.String())
	}
	return src, nil
}

func writeRuntimePredicateField(b *bytes.Buffer, name string, predicate *schema.RowPredicate) {
	if predicate == nil {
		return
	}
	fmt.Fprintf(b, ", %s: &", name)
	writeRuntimePredicate(b, *predicate)
}
func writeRuntimePredicate(b *bytes.Buffer, p schema.RowPredicate) {
	fmt.Fprintf(b, "rowPolicyRuntimePredicate{Kind:%s, Name:%s", strconv.Quote(string(p.Kind)), strconv.Quote(p.Name))
	if p.RelatedTable != "" {
		fmt.Fprintf(b, ", RelatedTable:%s, LocalColumn:%s, ForeignColumn:%s", strconv.Quote(p.RelatedTable), strconv.Quote(p.LocalColumn), strconv.Quote(p.ForeignColumn))
	}
	if len(p.Children) > 0 {
		b.WriteString(", Children: []rowPolicyRuntimePredicate{")
		for _, child := range p.Children {
			writeRuntimePredicate(b, child)
			b.WriteString(",")
		}
		b.WriteString("}")
	}
	b.WriteString("}")
}

func GeneratedRowPolicyFingerprint(plans []PostgresRowPolicyPlan) string {
	var parts []string
	for _, plan := range plans {
		for _, policy := range plan.Policies {
			parts = append(parts, plan.Table+":"+policy.Name+":"+string(policy.Command)+":"+policy.Using+":"+policy.WithCheck)
		}
	}
	sort.Strings(parts)
	return generatedRowPolicyName(strings.Join(parts, "|"), "state")
}

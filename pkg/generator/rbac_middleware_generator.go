package generator

import (
	"bytes"
	"fmt"
	"go/format"
	"text/template"
)

var loadRolesTemplate = template.Must(template.New("load_roles").Parse(`// Code generated by Pickle. DO NOT EDIT.
package middleware

import (
	"database/sql"

	pickle "{{ .HTTPImport }}"
)

// loadRolesFromDB queries role_user JOIN roles for the given user ID and
// returns the user's role memberships. Wired to pickle.RoleLoaderFunc at init.
func loadRolesFromDB(userID string) ([]pickle.RoleInfo, error) {
	rows, err := pickle.DB.Query(` + "`" + `
		SELECT r.slug, r.manages
		FROM role_user ru
		JOIN roles r ON r.id = ru.role_id
		WHERE ru.user_id = $1
	` + "`" + `, userID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var roles []pickle.RoleInfo
	for rows.Next() {
		var ri pickle.RoleInfo
		var manages sql.NullBool
		if err := rows.Scan(&ri.Slug, &manages); err != nil {
			return nil, err
		}
		ri.Manages = manages.Valid && manages.Bool
		roles = append(roles, ri)
	}
	return roles, rows.Err()
}

func init() {
	pickle.RoleLoaderFunc = loadRolesFromDB
}
`))

// GenerateLoadRolesMiddleware produces the load_roles_gen.go file for the
// project's middleware package. It wires pickle.RoleLoaderFunc to a concrete
// implementation that queries the role_user and roles tables.
func GenerateLoadRolesMiddleware(httpImport string) ([]byte, error) {
	data := struct {
		HTTPImport string
	}{
		HTTPImport: httpImport,
	}

	var buf bytes.Buffer
	if err := loadRolesTemplate.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("executing load_roles template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting load_roles source: %w", err)
	}
	return formatted, nil
}

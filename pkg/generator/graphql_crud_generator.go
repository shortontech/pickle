// writeNestedCreateMutation generates a parent-scoped create mutation for child tables.
// e.g., createPost(userId: ID!, input: CreatePostInput!) sets the FK automatically.
func writeNestedCreateMutation(b *bytes.Buffer, tbl *schema.Table, rel SchemaRelationship) {
	structName := tableToStructName(tbl.Name)
	parentSingular := strings.TrimSuffix(rel.ParentTable, "s")
	fkCol := parentSingular + "_id"
	parentArgName := snakeToCamel(parentSingular) + "Id"
	parentStructName := tableToStructName(rel.ParentTable)

	b.WriteString(fmt.Sprintf("func (r *RootResolver) crudCreateNested%s(ctx *ResolveContext, field Field) (any, error) {\n", structName))
	b.WriteString("\tif !ctx.IsAuthenticated() {\n")
	b.WriteString(fmt.Sprintf("\t\treturn nil, Unauthenticated(\"create%s: authentication required\")\n", structName))
	b.WriteString("\t}\n\n")

	// Extract parent ID
	b.WriteString(fmt.Sprintf("\tparentIDStr, ok := field.Args[\"%s\"].(string)\n", parentArgName))
	b.WriteString("\tif !ok {\n")
	b.WriteString(fmt.Sprintf("\t\treturn nil, BadInput(\"create%s: %s argument required\")\n", structName, parentArgName))
	b.WriteString("\t}\n")
	b.WriteString("\tparentID, err := uuid.Parse(parentIDStr)\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString(fmt.Sprintf("\t\treturn nil, BadInput(\"create%s: invalid %s\")\n", structName, parentArgName))
	b.WriteString("\t}\n\n")

	// Verify parent exists
	b.WriteString(fmt.Sprintf("\t_, err = models.Query%s().WhereID(parentID).First()\n", parentStructName))
	b.WriteString("\tif err != nil {\n")
	b.WriteString(fmt.Sprintf("\t\treturn nil, NotFound(\"%s\")\n", parentSingular))
	b.WriteString("\t}\n\n")

	b.WriteString(fmt.Sprintf("\tinput, err := extractCreate%sInput(field.Args[\"input\"])\n", structName))
	b.WriteString("\tif err != nil {\n\t\treturn nil, BadInput(err.Error())\n\t}\n\n")

	b.WriteString(fmt.Sprintf("\tif err := validate%sConstraints(input); err != nil {\n\t\treturn nil, err\n\t}\n\n", structName))

	b.WriteString("\tif err := validateInput(input); err != nil {\n\t\treturn nil, err\n\t}\n\n")

	b.WriteString(fmt.Sprintf("\trecord := &models.%s{}\n", structName))

	// Set FK from parent
	b.WriteString(fmt.Sprintf("\trecord.%s = parentID\n", snakeToPascal(fkCol)))

	// Set owner column from auth if present
	ownerCol := ownerColumn(tbl)
	if ownerCol != nil && ownerCol.Name != fkCol {
		goField := snakeToPascal(ownerCol.Name)
		if ownerCol.Type == schema.UUID {
			b.WriteString(fmt.Sprintf("\townerID, err := uuid.Parse(ctx.UserID())\n"))
			b.WriteString("\tif err != nil {\n")
			b.WriteString(fmt.Sprintf("\t\treturn nil, BadInput(\"create%s: invalid owner ID\")\n", structName))
			b.WriteString("\t}\n")
			b.WriteString(fmt.Sprintf("\trecord.%s = ownerID\n", goField))
		} else {
			b.WriteString(fmt.Sprintf("\trecord.%s = ctx.UserID()\n", goField))
		}
	}

	// Set remaining fields from input
	for _, col := range tbl.Columns {
		if isExcludedFromInput(col) {
			continue
		}
		if col.Name == fkCol {
			continue // already set from parent
		}
		if ownerCol != nil && col.Name == ownerCol.Name {
			continue // already set from auth
		}
		goFieldName := snakeToPascal(col.Name)
		conv := inputToModelConversion(col, fmt.Sprintf("input.%s", goFieldName), false)

		if col.IsNullable || col.DefaultValue != nil {
			b.WriteString(fmt.Sprintf("\tif input.%s != nil {\n", goFieldName))
			conv = inputToModelConversion(col, fmt.Sprintf("*input.%s", goFieldName), col.IsNullable)
			b.WriteString(fmt.Sprintf("\t\trecord.%s = %s\n", goFieldName, conv))
			b.WriteString("\t}\n")
		} else {
			b.WriteString(fmt.Sprintf("\trecord.%s = %s\n", goFieldName, conv))
		}
	}

	b.WriteString(fmt.Sprintf("\n\tif err := models.Query%s().Create(record); err != nil {\n\t\treturn nil, InternalError(err.Error())\n\t}\n\n", structName))
	b.WriteString(fmt.Sprintf("\treturn r.resolve%sObject(ctx, record, field.Selections)\n", structName))
	b.WriteString("}\n\n")
}

// isFKToParent returns true if the column is a foreign key to the parent table in a nesting relationship.
func isFKToParent(col *schema.Column, rel SchemaRelationship) bool {
	parentSingular := strings.TrimSuffix(rel.ParentTable, "s")
	return col.Name == parentSingular+"_id"
}

// writeConstraintValidators generates validation functions derived from column constraints.
func writeConstraintValidators(b *bytes.Buffer, tables []*schema.Table) {
	for _, tbl := range tables {
		if tbl.IsImmutable {
			continue
		}
		structName := tableToStructName(tbl.Name)

		// Validate constraints for CreateInput
		b.WriteString(fmt.Sprintf("func validate%sConstraints(input any) error {\n", structName))
		b.WriteString(fmt.Sprintf("\tswitch v := input.(type) {\n"))

		// CreateInput validation
		createInputType := "Create" + structName + "Input"
		b.WriteString(fmt.Sprintf("\tcase *%s:\n", createInputType))
		hasCreateValidation := false
		for _, col := range tbl.Columns {
			if isExcludedFromInput(col) {
				continue
			}
			goField := snakeToPascal(col.Name)
			fieldLabel := snakeToCamel(col.Name)

			// String length constraint
			if col.Length > 0 && (col.Type == schema.String || col.Type == schema.Text) {
				hasCreateValidation = true
				isPtr := col.IsNullable || col.DefaultValue != nil
				if isPtr {
					b.WriteString(fmt.Sprintf("\t\tif v.%s != nil && len(*v.%s) > %d {\n", goField, goField, col.Length))
				} else {
					b.WriteString(fmt.Sprintf("\t\tif len(v.%s) > %d {\n", goField, col.Length))
				}
				b.WriteString(fmt.Sprintf("\t\t\treturn BadInput(\"%s: maximum length %d\")\n", fieldLabel, col.Length))
				b.WriteString("\t\t}\n")
			}

			// UUID format validation for FK columns
			if col.ForeignKeyTable != "" && col.Type == schema.UUID {
				hasCreateValidation = true
				isPtr := col.IsNullable || col.DefaultValue != nil
				if isPtr {
					b.WriteString(fmt.Sprintf("\t\tif v.%s != nil {\n", goField))
					b.WriteString(fmt.Sprintf("\t\t\tif _, err := uuid.Parse(*v.%s); err != nil {\n", goField))
					b.WriteString(fmt.Sprintf("\t\t\t\treturn BadInput(\"%s: invalid UUID format\")\n", fieldLabel))
					b.WriteString("\t\t\t}\n")
					b.WriteString("\t\t}\n")
				} else {
					b.WriteString(fmt.Sprintf("\t\tif _, err := uuid.Parse(v.%s); err != nil {\n", goField))
					b.WriteString(fmt.Sprintf("\t\t\treturn BadInput(\"%s: invalid UUID format\")\n", fieldLabel))
					b.WriteString("\t\t}\n")
				}
			}
		}
		if !hasCreateValidation {
			b.WriteString("\t\t_ = v\n")
		}

		// UpdateInput validation
		if !tbl.IsAppendOnly {
			updateInputType := "Update" + structName + "Input"
			b.WriteString(fmt.Sprintf("\tcase *%s:\n", updateInputType))
			hasUpdateValidation := false
			for _, col := range tbl.Columns {
				if isExcludedFromInput(col) {
					continue
				}
				goField := snakeToPascal(col.Name)
				fieldLabel := snakeToCamel(col.Name)

				if col.Length > 0 && (col.Type == schema.String || col.Type == schema.Text) {
					hasUpdateValidation = true
					b.WriteString(fmt.Sprintf("\t\tif v.%s != nil && len(*v.%s) > %d {\n", goField, goField, col.Length))
					b.WriteString(fmt.Sprintf("\t\t\treturn BadInput(\"%s: maximum length %d\")\n", fieldLabel, col.Length))
					b.WriteString("\t\t}\n")
				}

				if col.ForeignKeyTable != "" && col.Type == schema.UUID {
					hasUpdateValidation = true
					b.WriteString(fmt.Sprintf("\t\tif v.%s != nil {\n", goField))
					b.WriteString(fmt.Sprintf("\t\t\tif _, err := uuid.Parse(*v.%s); err != nil {\n", goField))
					b.WriteString(fmt.Sprintf("\t\t\t\treturn BadInput(\"%s: invalid UUID format\")\n", fieldLabel))
					b.WriteString("\t\t\t}\n")
					b.WriteString("\t\t}\n")
				}
			}
			if !hasUpdateValidation {
				b.WriteString("\t\t_ = v\n")
			}
		}

		b.WriteString("\t}\n")
		b.WriteString("\treturn nil\n")
		b.WriteString("}\n\n")
	}
}

// GenerateGraphQLCRUDSchema extends the SDL with nested resource mutations.
// Returns additional mutation fields to append to the schema.
func GenerateNestedMutationSDL(tables []*schema.Table, relationships []SchemaRelationship) string {
	childToParent := map[string]SchemaRelationship{}
	for _, rel := range relationships {
		childToParent[rel.ChildTable] = rel
	}

	var b strings.Builder
	for _, tbl := range tables {
		if tbl.IsImmutable {
			continue
		}
		rel, isChild := childToParent[tbl.Name]
		if !isChild {
			continue
		}
		structName := tableToStructName(tbl.Name)
		parentSingular := strings.TrimSuffix(rel.ParentTable, "s")
		parentArgName := snakeToCamel(parentSingular) + "Id"

		b.WriteString(fmt.Sprintf("  createNested%s(%s: ID!, input: Create%sInput!): %s! @auth\n",
			structName, parentArgName, structName, structName))
	}
	return b.String()
}

// ColumnConstraints extracts validation constraints from column definitions
// to generate validate tags for input structs when no request struct exists.
func ColumnConstraints(col *schema.Column) string {
	var parts []string

	// Required on create: NOT NULL without default
	if !col.IsNullable && col.DefaultValue == nil {
		parts = append(parts, "required")
	}

	// String length
	if col.Length > 0 && (col.Type == schema.String || col.Type == schema.Text) {
		parts = append(parts, fmt.Sprintf("max=%d", col.Length))
	}

	// UUID format
	if col.Type == schema.UUID {
		parts = append(parts, "uuid")
	}

	return strings.Join(parts, ",")
}

// HasCRUDOverride checks if a user-written resolver file exists for a specific
// resource, implementing the per-resource override pattern.
func HasCRUDOverride(graphqlDir, tableName string) bool {
	singular := strings.TrimSuffix(tableName, "s")
	return hasOverride(graphqlDir, singular+"_resolvers.go")
}

func TestGenerateRBACGate_Basic(t *testing.T) {
	out, err := GenerateRBACGate(
		"ban_user",
		[]string{"moderator", "support"},
		[]string{"admin"},
		"2026_03_01_create_roles",
		"pickle",
	)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	src := string(out)

	if !strings.Contains(src, "func CanBanUser(") {
		t.Error("expected CanBanUser function declaration")
	}
	if !strings.Contains(src, `"admin"`) {
		t.Error("expected admin in manages roles check")
	}
	if !strings.Contains(src, `"moderator", "support"`) {
		t.Error("expected moderator, support in allowed roles check")
	}
	// Manages check should appear before allowed roles
	managesIdx := strings.Index(src, "Manages roles")
	allowedIdx := strings.Index(src, "Specific roles")
	if managesIdx < 0 || allowedIdx < 0 {
		t.Error("expected both manages and specific role comments")
	}
	if managesIdx > allowedIdx {
		t.Error("manages roles check should come before specific roles check")
	}
}


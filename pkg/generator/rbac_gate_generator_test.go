func TestGateActionToFuncName(t *testing.T) {
	tests := []struct {
		input, want string
	}{
		{"ban_user", "BanUser"},
		{"delete", "Delete"},
		{"edit_post", "EditPost"},
		{"view", "View"},
		{"create_draft_post", "CreateDraftPost"},
	}
	for _, tt := range tests {
		got := gateActionToFuncName(tt.input)
		if got != tt.want {
			t.Errorf("gateActionToFuncName(%q) = %q, want %q", tt.input, got, tt.want)
		}
	}
}

package blade

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"strings"
	"unicode"
)

type fieldKind int

const (
	scalar fieldKind = iota
	boolean
	object
	collection
)

type field struct {
	Name     string
	Kind     fieldKind
	Children map[string]*field
}

// CompileGo emits a typed renderer into the application's HTTP package.
func CompileGo(packageName string, docs []*Document) ([]byte, error) {
	return CompileGoWithAssets(packageName, docs, nil)
}

// CompileGoWithAssets resolves static asset nodes against a generated manifest.
func CompileGoWithAssets(packageName string, docs []*Document, assets map[string]string) ([]byte, error) {
	resolved, err := Resolve(docs)
	if err != nil {
		return nil, err
	}
	docs = resolved
	var b bytes.Buffer
	fmt.Fprintf(&b, "// Code generated by Pickle. DO NOT EDIT.\n\npackage %s\n\nimport (\n\t\"html\"\n\t\"strings\"\n)\n\n", packageName)
	for _, doc := range docs {
		root := &field{Children: map[string]*field{}}
		if err := infer(doc.Nodes, root, nil); err != nil {
			return nil, fmt.Errorf("%s: %w", doc.Name, err)
		}
		name := exportedName(doc.Name)
		writeStruct(&b, name+"Data", root, "")
		fmt.Fprintf(&b, "func %s(ctx *Context, data %sData) Response {\n\tvar out strings.Builder\n", name, name)
		if err := writeNodes(&b, doc.Nodes, "data", map[string]string{}, assets, "\t"); err != nil {
			return nil, fmt.Errorf("%s: %w", doc.Name, err)
		}
		fmt.Fprintf(&b, "\treturn renderedViewResponse(ctx, out.String())\n}\n\n")
	}
	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting generated views: %w\n%s", err, b.String())
	}
	return formatted, nil
}

func infer(nodes []Node, root *field, locals map[string]*field) error {
	if locals == nil {
		locals = map[string]*field{}
	}
	for _, n := range nodes {
		switch n := n.(type) {
		case Escaped:
			if err := require(root, locals, n.Path, scalar); err != nil {
				return err
			}
		case If:
			if err := require(root, locals, n.Condition, boolean); err != nil {
				return err
			}
			if err := infer(n.Then, root, locals); err != nil {
				return err
			}
			if err := infer(n.Else, root, locals); err != nil {
				return err
			}
		case RouteIs:
			if err := infer(n.Then, root, locals); err != nil {
				return err
			}
			if err := infer(n.Else, root, locals); err != nil {
				return err
			}
		case ForEach:
			f, err := requireField(root, locals, n.Collection, collection)
			if err != nil {
				return err
			}
			next := cloneLocals(locals)
			next[n.Item] = f
			if err := infer(n.Body, root, next); err != nil {
				return err
			}
		}
	}
	return nil
}

func require(root *field, locals map[string]*field, path []string, kind fieldKind) error {
	_, err := requireField(root, locals, path, kind)
	return err
}
func requireField(root *field, locals map[string]*field, path []string, kind fieldKind) (*field, error) {
	cur := root
	start := 0
	if local := locals[path[0]]; local != nil {
		cur = local
		start = 1
	}
	for i := start; i < len(path); i++ {
		if cur.Children == nil {
			cur.Children = map[string]*field{}
		}
		child := cur.Children[path[i]]
		want := object
		if i == len(path)-1 {
			want = kind
		}
		if child == nil {
			child = &field{Name: path[i], Kind: want, Children: map[string]*field{}}
			cur.Children[path[i]] = child
		} else if i == len(path)-1 && child.Kind != want {
			return nil, fmt.Errorf("%s is used with incompatible types", strings.Join(path, "."))
		}
		cur = child
	}
	return cur, nil
}

func cloneLocals(in map[string]*field) map[string]*field {
	out := map[string]*field{}
	for k, v := range in {
		out[k] = v
	}
	return out
}

func writeStruct(b *bytes.Buffer, name string, f *field, indent string) {
	fmt.Fprintf(b, "type %s struct {\n", name)
	keys := sortedFields(f.Children)
	for _, key := range keys {
		child := f.Children[key]
		fmt.Fprintf(b, "\t%s ", exportedName(child.Name))
		writeType(b, child, "\t")
		b.WriteByte('\n')
	}
	b.WriteString("}\n\n")
}

func writeType(b *bytes.Buffer, f *field, indent string) {
	switch f.Kind {
	case scalar:
		b.WriteString("string")
	case boolean:
		b.WriteString("bool")
	case object, collection:
		if f.Kind == collection {
			b.WriteString("[]")
		}
		b.WriteString("struct {\n")
		for _, key := range sortedFields(f.Children) {
			child := f.Children[key]
			fmt.Fprintf(b, "%s\t%s ", indent, exportedName(child.Name))
			writeType(b, child, indent+"\t")
			b.WriteByte('\n')
		}
		fmt.Fprintf(b, "%s}", indent)
	}
}

func writeNodes(b *bytes.Buffer, nodes []Node, root string, locals map[string]string, assets map[string]string, indent string) error {
	for _, node := range nodes {
		switch n := node.(type) {
		case Text:
			fmt.Fprintf(b, "%sout.WriteString(%q)\n", indent, n.Value)
		case Escaped:
			fmt.Fprintf(b, "%sout.WriteString(html.EscapeString(%s))\n", indent, goPath(root, locals, n.Path))
		case Asset:
			url, ok := assets[n.Name]
			if !ok {
				return fmt.Errorf("asset %q does not exist", n.Name)
			}
			fmt.Fprintf(b, "%sout.WriteString(%q)\n", indent, url)
		case RouteURL:
			fmt.Fprintf(b, "%sout.WriteString(html.EscapeString(ctx.RouteURL(%q, nil)))\n", indent, n.Name)
		case RouteIs:
			fmt.Fprintf(b, "%sif ctx.RouteIs(%q) {\n", indent, n.Pattern)
			if err := writeNodes(b, n.Then, root, locals, assets, indent+"\t"); err != nil {
				return err
			}
			if len(n.Else) > 0 {
				fmt.Fprintf(b, "%s} else {\n", indent)
				if err := writeNodes(b, n.Else, root, locals, assets, indent+"\t"); err != nil {
					return err
				}
			}
			fmt.Fprintf(b, "%s}\n", indent)
		case If:
			fmt.Fprintf(b, "%sif %s {\n", indent, goPath(root, locals, n.Condition))
			if err := writeNodes(b, n.Then, root, locals, assets, indent+"\t"); err != nil {
				return err
			}
			if len(n.Else) > 0 {
				fmt.Fprintf(b, "%s} else {\n", indent)
				if err := writeNodes(b, n.Else, root, locals, assets, indent+"\t"); err != nil {
					return err
				}
			}
			fmt.Fprintf(b, "%s}\n", indent)
		case ForEach:
			local := "item" + fmt.Sprint(n.Span.Start)
			fmt.Fprintf(b, "%sfor _, %s := range %s {\n", indent, local, goPath(root, locals, n.Collection))
			next := map[string]string{}
			for k, v := range locals {
				next[k] = v
			}
			next[n.Item] = local
			if err := writeNodes(b, n.Body, root, next, assets, indent+"\t"); err != nil {
				return err
			}
			fmt.Fprintf(b, "%s}\n", indent)
		}
	}
	return nil
}

func goPath(root string, locals map[string]string, path []string) string {
	base := root
	start := 0
	if local := locals[path[0]]; local != "" {
		base = local
		start = 1
	}
	for _, part := range path[start:] {
		base += "." + exportedName(part)
	}
	return base
}

func exportedName(s string) string {
	parts := strings.FieldsFunc(strings.TrimSuffix(s, ".blade.php"), func(r rune) bool { return r == '/' || r == '.' || r == '-' || r == '_' })
	var b strings.Builder
	for _, part := range parts {
		rr := []rune(part)
		if len(rr) > 0 {
			rr[0] = unicode.ToUpper(rr[0])
			b.WriteString(string(rr))
		}
	}
	return b.String()
}
func sortedFields(fields map[string]*field) []string {
	keys := make([]string, 0, len(fields))
	for k := range fields {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

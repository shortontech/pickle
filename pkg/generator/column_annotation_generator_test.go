package generator

import (
	"strings"
	"testing"
)

func TestSlugToPascal(t *testing.T) {
	tests := []struct {
		slug   string
		expect string
	}{
		{"compliance", "Compliance"},
		{"support_lead", "SupportLead"},
		{"a", "A"},
		{"admin-user", "AdminUser"},
		{"super_admin_role", "SuperAdminRole"},
		{"x_y_z", "XYZ"},
	}
	for _, tt := range tests {
		got := SlugToPascal(tt.slug)
		if got != tt.expect {
			t.Errorf("SlugToPascal(%q) = %q, want %q", tt.slug, got, tt.expect)
		}
	}
}

func TestGenerateColumnAnnotations(t *testing.T) {
	roles := []RoleAnnotation{
		{Slug: "compliance", PascalName: "Compliance"},
		{Slug: "support_lead", PascalName: "SupportLead"},
	}

	src, err := GenerateColumnAnnotations("migrations", roles)
	if err != nil {
		t.Fatal(err)
	}

	content := string(src)

	if !strings.Contains(content, "func (c *Column) ComplianceSees() *Column") {
		t.Error("expected ComplianceSees method")
	}
	if !strings.Contains(content, "func (c *Column) SupportLeadSees() *Column") {
		t.Error("expected SupportLeadSees method")
	}
	if !strings.Contains(content, `c.RoleSees("compliance")`) {
		t.Error("expected RoleSees call with slug")
	}
	if !strings.Contains(content, "// Code generated by Pickle. DO NOT EDIT.") {
		t.Error("expected generated header")
	}
}

func TestGenerateColumnAnnotationsEmpty(t *testing.T) {
	src, err := GenerateColumnAnnotations("migrations", nil)
	if err != nil {
		t.Fatal(err)
	}
	if src != nil {
		t.Error("expected nil for empty roles")
	}
}

func TestGenerateColumnAnnotationsChainable(t *testing.T) {
	roles := []RoleAnnotation{
		{Slug: "editor", PascalName: "Editor"},
	}
	src, err := GenerateColumnAnnotations("migrations", roles)
	if err != nil {
		t.Fatal(err)
	}
	// Method returns *Column — chainable
	if !strings.Contains(string(src), "*Column") {
		t.Error("expected *Column return type for chainability")
	}
}
